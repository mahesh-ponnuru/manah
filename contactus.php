  document.addEventListener("DOMContentLoaded", () => {
  const headings = document.querySelectorAll('.animated-heading');
  let currentIndex = 0;

  // Duration each slide will be visible (in milliseconds)
  const slideDuration = 2000; // 2 seconds per slide
  const slideInterval = 1000; // 1 second interval between slides

  function showNextHeading() {
    // Hide the current heading after its animation is complete
    headings[currentIndex].style.visibility = 'hidden'; // Hide the current heading

    // Move to the next heading (loop back to 0 if at the end)
    currentIndex = (currentIndex + 1) % headings.length;
    console.log("length", headings.length, currentIndex);

    // Show the next heading
    headings[currentIndex].style.visibility = 'visible'; // Show the next heading
  }

  // Initially show the first heading
  headings[currentIndex].style.visibility = 'visible';

  // Change heading every `slideDuration` + `slideInterval`
  setInterval(showNextHeading, slideDuration + slideInterval); // Timing for each slide
});


